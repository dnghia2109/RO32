-- DÙNG IF EXISTS để xóa db nếu nó đã tồn tại
DROP DATABASE IF EXISTS `Testing_System` ;
CREATE DATABASE `Testing_System`;
USE `Testing_System`;

-- Table 1: Department
CREATE TABLE `Department`
(
	DepartmentID TINYINT UNSIGNED PRIMARY KEY AUTO_INCREMENT, -- dùng UNSIGNED(số không có dấu) cho id
	DepartmentName VARCHAR(30)
);


-- Table 2: Position
CREATE TABLE `Position`
(
	PositionID TINYINT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
	PositionName NVARCHAR(30)
);


-- Table 3: Account
/*AccountID: định danh của User (auto increment)
 Email:
 Username:
 FullName:
 DepartmentID: phòng ban của user trong hệ thống
 PositionID: chức vụ của User
 CreateDate: ngày tạo tài khoản*/
CREATE TABLE `Account`
(
	AccountID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
	Email VARCHAR(60) UNIQUE NOT NULL,
	Username VARCHAR(30) UNIQUE NOT NULL,
	FullName VARCHAR(30), 
	DepartmentID TINYINT UNSIGNED,
	PositionID TINYINT UNSIGNED,
	CreateDate DATE DEFAULT(NOW()),
	FOREIGN KEY (DepartmentID) REFERENCES `Department`(DepartmentID),
	FOREIGN KEY (PositionID) REFERENCES `Position`(PositionID)
);

/*ALTER TABLE `Account`
